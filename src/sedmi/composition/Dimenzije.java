package sedmi.composition;


public class Dimenzije {
    private int duzina;
    private int sirina;
    private int visina;

    public Dimenzije(int duzina, int sirina, int visina) {
        this.duzina = duzina;
        this.sirina = sirina;
        this.visina = visina;
    }

    public int getDuzina() {
        return duzina;
    }

    public int getSirina() {
        return sirina;
    }

    public int getVisina() {
        return visina;
    }
    
    
}
